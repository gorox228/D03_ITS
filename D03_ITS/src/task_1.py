"""
Пример кода с ошибкой № 1
А да, это пример многострочного комментария. Комментарии не выполняются интерпретатором.

Изучи все операции, запиши их на листочке, вычисли и проверь результат.
"""


number_1 = 3
number_2 = number_1 ** 2 + int('10') / 5  # Кстати комментарии могут быть и однострочными, как этот
number_3 = number_1 + number_2 % 2 + 1
number_4 = number_1 // 3 + number_3

print(number_4)
